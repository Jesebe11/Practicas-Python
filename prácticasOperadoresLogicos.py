#Operaciones Matemáticas

a = int(input('Ingrese un número A:'))
b = int(input('Ingrese un número B:'))

print('Suma:', a + b )
print('Resta:', a - b )
print('Multiplicacion:', a * b )
print('Division:', a / b )
print('Division entero:', a // b )
print('Residuo:', a % b )
print('¿A es Mayor que B?:', a > b )
print('¿A es Menor que B?:', a < b )
print('¿A es Igual que b?:', a == b )